    private void BtnConvertirTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConvertirTActionPerformed
        
        double valorT = Double.parseDouble(texto_temperatura.getText());
        int t1 = temperatura1.getSelectedIndex();
        int t2 = temperatura2.getSelectedIndex();
        
        OperacionesDeTemperatura puente2 = new OperacionesDeTemperatura();
        
        puente2.setValor(valorT);
        puente2.setT1(t1);
        puente2.setT2(t2);
        
        double rt = puente2.convetirT();
        
        ResultadoT.setText(" ");
        
        ResultadoT.append( " " + rt);
        
        
        
        
    }//GEN-LAST:event_BtnConvertirTActionPerformed

